# Тема 1: Условные операторы и тернарные операторы

# Условные операторы позволяют программе принимать решения на основе заданных условий. Они проверяют истинность выражений и выполняют различные действия в зависимости от результата.

# Условный оператор if

# Синтаксис:

# if выражение:
#     инструкции
# Пример:


# a = 10
# if a > 5:
#     print("a больше 5")
# В этом примере, если значение переменной a больше 5, то программа выведет на экран сообщение "a больше 5". Если условие не истинно, программа ничего не выведет.

# Условный оператор if-else

# Синтаксис:

# if выражение:
#     инструкции1
# else:
#     инструкции2
# Пример:

# a = 3
# if a > 5:
#     print("a больше 5")
# else:
#     print("a меньше или равно 5")
# В этом примере, если значение переменной a больше 5, программа выведет на экран сообщение "a больше 5", а если значение переменной a меньше или равно 5, программа выведет сообщение "a меньше или равно 5".

# Тернарный оператор

# Тернарный оператор - это сокращенная форма записи условного оператора. Он состоит из трех операндов: условия, значения, которое возвращается, если условие истинно, и значения, которое возвращается, если условие ложно.

# Синтаксис:

# значение1 if условие else значение2
# Пример:

# a = 10
# b = 20
# max_value = a if a > b else b
# print(max_value)
# В этом примере, если a больше, чем b, то значение a будет присвоено переменной max_value, в противном случае значение b будет присвоено переменной max_value.

